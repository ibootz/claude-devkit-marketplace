// 子代理版：只带对子代理自身有意义的部分（上下文/协作/表达），
// 去掉"思维模式全表"和"Agent 派发大路由表"以省 token；子代理如需再派下一层，
// 上方"二、"已含在飞 16 上限与 2 层嵌套约束。
function buildSubagentPrompt() {
  return [
    '# AI 工作纪律（子代理版）',
    '',
    '你是被父代理通过 Agent 工具派发出来的子代理。以下纪律同样约束你的产出与协作行为。',
    '特别地，二、中的「完成回执复述」对你首先意味着：你的最终回复必须是结构化回执——改了哪些文件、关键决策、阻塞点、需要父代理跟进的事项；只回「已完成」视为不合格。',
    '',
    SECTION_CONTEXT,
    '',
    SECTION_SUBAGENT,
    '',
    SECTION_EXPRESSION,
  ].join('\n')
}

function main() {
  const event = readEvent()
  const name = event && event.hook_event_name

  const isSubagent = name === 'SubagentStart'
  const hookEventName = isSubagent ? 'SubagentStart' : 'UserPromptSubmit'
  const additionalContext = isSubagent ? buildSubagentPrompt() : buildMainPrompt()

  const output = {
    hookSpecificOutput: {
      hookEventName,
      additionalContext,
    },
  }

  process.stdout.write(JSON.stringify(output) + '\n')
  process.exit(0)
}

main()
